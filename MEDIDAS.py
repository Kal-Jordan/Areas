
A = float(input("Digite a medida A: "))
B = float(input("Digite a medida B: "))
C = float(input("Digite a medida C: "))

areaq = A * A
areatri = (A * B) / 2
areatrap = (A + B) * C / 2

print(f"AREA DO QUADRADO = {areaq:.4f}")
print(f"AREA DO TRIANGULO = {areatri:.4f}")
print(f"AREA DO TRAPEZIO = {areatrap:.4f}")


